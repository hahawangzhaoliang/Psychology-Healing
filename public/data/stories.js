/**
 * 根据场景获取推荐故事
 * @param {string} scenario - morning | noon | evening | sleep
 * @param {number} count - 返回数量
 * @returns {Array}
 */
function getStoriesByScenario(scenario, count = 1) {
  const filtered = STORIES.filter(s => s.scenario === scenario);
  return filtered.sort(() => Math.random() - 0.5).slice(0, count);
}

/**
 * 获取适合特定情绪的故事
 * @param {string} emotionKey - joy | sadness | anger | fear | anxiety | calm
 * @param {number} count
 * @returns {Array}
 */
function getStoriesByEmotion(emotionKey, count = 1) {
  const emotionMap = {
    joy: ['希望', '温暖', '美好'],
    sadness: ['温暖', '疗愈', '接纳'],
    anger: ['平静', '和解', '放下'],
    fear: ['安全', '勇气', '保护'],
    anxiety: ['安心', '暂停', '接纳'],
    calm: ['宁静', '当下', '正念'],
  };
  const keywords = emotionMap[emotionKey] || ['疗愈'];
  const filtered = STORIES.filter(s =>
    s.tags.some(tag => keywords.some(k => tag.includes(k)))
  );
  return filtered.sort(() => Math.random() - 0.5).slice(0, count);
}
